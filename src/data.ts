// Helper to check badge triggers based on progress state
export function getEligibleBadges(progress: any): string[] {
  const unlocked = [...progress.unlockedBadges];

  if (progress.learnedLetters.length >= 1 && !unlocked.includes("first_step")) {
    unlocked.push("first_step");
  }
  if (progress.learnedLetters.length >= 13 && !unlocked.includes("alpha_half")) {
    unlocked.push("alpha_half");
  }
  if (progress.learnedLetters.length >= 26 && !unlocked.includes("alpha_all")) {
    unlocked.push("alpha_all");
  }
  
  // Quiz Whiz
  const perfectQuizzes = progress.quizScores.some((q: any) => q.score === q.total && q.total >= 3);
  if (perfectQuizzes && !unlocked.includes("perfect_score")) {
    unlocked.push("perfect_score");
  }

  // Magic Tracer - count traced letters
  const tracedCount = Object.keys(progress.tracingProgress).length;
  if (tracedCount >= 5 && !unlocked.includes("trace_pro")) {
    unlocked.push("trace_pro");
  }

  // Voice Learning - we can track standard flag in parent state
  return unlocked;
}
